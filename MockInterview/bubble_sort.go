// Like bubbles rising: Each pass, largest sinks to end.
// I loop n times, inner compares i to i+1 and swaps if needed.
// Early stop if sorted
package mockinterview

func bubbleSort(arr []int) {
	n := len(arr)
	for i := 0; i < n-1; i++ {
		swapped := false
		for j := 0; j < n-i-1; j++ {
			if arr[j] > arr[j+1] {
				arr[j], arr[j+1] = arr[j+1], arr[j]
				swapped = true
			}
		}
		if !swapped {
			break
		}
	}
}
